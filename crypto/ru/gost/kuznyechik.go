// kuznyechikC contains the iteration constants for key schedule.
var kuznyechikC [32][16]byte

func init() {
	// Generate iteration constants C[i] = L(i+1)
	for i := 0; i < 32; i++ {
		var c [16]byte
		c[15] = byte(i + 1)
		kuznyechikL(&c)
		kuznyechikC[i] = c
	}
}
