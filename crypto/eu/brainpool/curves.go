// params is an implementation of elliptic.CurveParams.
type params struct {
	name string
	bitSize int
	p, a, b, gx, gy, n *big.Int
	h int
}

func (p *params) Name() string { return p.name }
func (p *params) Params() *elliptic.CurveParams {
	return &elliptic.CurveParams{
		P: p.p,
		A: p.a,
		B: p.b,
		G: elliptic.Point{X: p.gx, Y: p.gy},
		N: p.n,
		H: p.h,
		BitSize: p.bitSize,
		Name: p.name,
	}
}

